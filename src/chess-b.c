void qsortMoveList(MoveList* ml, int* vals, int elements) {

  int piv, L, R;
	int beg[128], end[128];
	int i=0;
	Move tempMove;

  beg[0]=0;
	end[0]=elements;
  while (i>=0) {
    L=beg[i];
		R=end[i]-1;
    if (L<R) {
      piv=vals[L];
			tempMove =ml->list[L];
			if (i==127) return;
      while (L<R) {
        while (vals[R]>=piv && L<R)
					R--;
				if (L<R) {
					ml->list[L]=ml->list[R];
					vals[L++]=vals[R];
				}
        while (vals[L]<=piv && L<R)
					L++;
				if (L<R) {
					ml->list[R]=ml->list[L];
					vals[R--]=vals[L];
				}
			}
			ml->list[L]=tempMove;
      vals[L]=piv;
			beg[i+1]=L+1;
			end[i+1]=end[i];
			end[i++]=L;
		}
    else {
      i--;
		}
	}
}


// Most value victim + least valuable attacker
void sortCapturesMVVLVA(MoveList* ml) {
	int length = ml->used;
	int values[length];
	int i;
	for(i=0; i<length; i++) {
		values[i] = PIECE_VALUES_ABS[ml->list[i].piece] - PIECE_VALUES_ABS[ml->list[i].cPiece];
	}
	qsortMoveList(ml, values, length);
}

// Most value victim
void sortCapturesMVV(MoveList* ml) {
	int length = ml->used;
	int values[length];
	int i;
	for(i=0; i<length; i++) {
		values[i] = -PIECE_VALUES_ABS[ml->list[i].cPiece];
	}
	qsortMoveList(ml, values, length);
}

