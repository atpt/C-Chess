Move ai(Position* p, IrrFlagStack* fs, int depth, int aiMode, int maxExtensions, int verbose) {
	MoveList ml;
	Move returnMove;
	genLegalMoves(&ml, p, fs);
	int numMoves = ml.used;
	if(numMoves == 0) {
		printf("Error: No legal moves.\n");
		freeMoveList(&ml);
		return returnMove;
	}
	int scores[numMoves];
	int bestScore, alpha, beta;
	int bestIndex = 0;
	Move *m;
	alpha = INT_MIN;
	beta = INT_MAX;
	if(p->player == WHITE) {
		// Maximising player
		bestScore = INT_MIN;
		for(int i=0; i<numMoves; i++) {
			m = &ml.list[i];
			makeMove(p, m, fs);
			// scores[i] = fastEvaluateMove(b, &ml.list[i], depth);
			switch(aiMode) {
				case DIRTY_AI:
					scores[i] = dirtyAlphaBeta(p, fs, depth, maxExtensions, alpha, beta);
				case NORMAL_AI:
				default:
					scores[i] = alphaBeta(p, fs, depth, maxExtensions, alpha, beta, m->cPiece);
			}
			unmakeMove(p, m, fs);
			if(verbose == TRUE) {
				// printf("\nFinal score for move: ");
				printf("(");
				outputMove(m, FALSE);
				printf(": %d) ", scores[i]);
			}
			if(scores[i] > bestScore) {
				bestScore = scores[i];
				bestIndex = i;
			}
			alpha = (alpha > bestScore) ? alpha : bestScore;
		}
	} else {
		// Minimising player
		bestScore = INT_MAX;
		for(int i=0; i<numMoves; i++) {
			m = &ml.list[i];
			makeMove(p, m, fs);
			// scores[i] = fastEvaluateMove(b, &ml.list[i], depth);
			switch(aiMode) {
				case DIRTY_AI:
					scores[i] = dirtyAlphaBeta(p, fs, depth, maxExtensions, alpha, beta);
				case NORMAL_AI:
				default:
					scores[i] = alphaBeta(p, fs, depth, maxExtensions, alpha, beta, m->cPiece);
			}
			unmakeMove(p, m, fs);
			if(verbose == TRUE) {
				printf("(");
				outputMove(m, FALSE);
				printf(": %d) ", scores[i]);
			}
			if(scores[i] < bestScore) {
				bestScore = scores[i];
				bestIndex = i;
			}
			beta = (beta < bestScore) ? beta : bestScore;
		}
	}
	returnMove = ml.list[bestIndex];
	if(verbose == TRUE) {
		printf("Best move: ");
		outputMove(&returnMove, FALSE);
		printf(" (%d)\n", bestScore);
	}
	freeMoveList(&ml);
	return returnMove;
}


